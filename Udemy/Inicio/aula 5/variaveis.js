/*
1 - Sempre recomendado utilizar let por ser mais moderno que var;
2 - Não podem ser utilizadas palavras reservadas do JS para a criação de variáveis;
3 - Colocar sempre nomes significativos para o código;
4 - Não pode começar o nome de uma variável com um número;
5 - Variáveis não podem conter traços ou espaços;
6 - Para declarações compostas, é utilizado o modelo camelCase. Exemplo: 'nomeCompleto';
7 - As variáveis são case-sensitive, ou seja, diferencia maiúsculas de minusculas. 
8 - Não pode redeclarar variáveis com a 'let';
*/

